        /// Indicating use swagger-style UI.
        /// </summary>
        Swagger,
        /// <summary>
        /// Indicating use Json-Handle as UI.
        /// </summary>
        JsonH,
        /// <summary>
        /// Indicating use Json-Editory as UI.
        /// </summary>
        JsonEditor,
    }
}
